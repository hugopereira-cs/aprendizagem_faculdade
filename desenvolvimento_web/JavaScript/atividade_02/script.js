function somar(a, b) {
    return a + b;
}
function subtrair(a, b) {
    return a - b;
}
function multiplicar(a, b) {
    return a * b;
}

function dividir(a, b) {
    if (b == 0) alert('Divisão por zero não é permitida.');
    return a / b;
}
function exponencial(a, b) {
    return Math.pow(a, b);
}
